#loop that counts up from 0 - 50 in increments of 1.
print("Counting up from 0 - 50 in increments of 1:")
for i in range (0,50,1):
    print(i, end=" ")
print("\n")

#loop that counts down from 50 - 0 in decrements of 1.
print("Counting down from 50 - 0 in decrements of 1:")
for i in range (50,-1,-1):
    print(i, end=" ")
print("\n")

